def _find_pyarmor(base_dir: str) -> str:
    """Locate the pyarmor executable; raise FileNotFoundError if absent."""
    for candidate in (
        os.path.join(base_dir, 'venv', 'Scripts', 'pyarmor.exe'),
        os.path.join(base_dir, 'venv', 'bin', 'pyarmor'),
        shutil.which('pyarmor') or '',
    ):
        if candidate and os.path.isfile(candidate):
            return candidate
    raise FileNotFoundError(
        "pyarmor not found. Install it with:  pip install pyarmor"
    )


def _run_pyarmor_file(
    pyarmor_bin: str,
    src_file: str,
    out_dir: str,
    runtime_dir: Optional[str] = None,
) -> bool:
    """
    Obfuscate *src_file* and place the result in *out_dir*.

    If *runtime_dir* is supplied (path to a pre-generated
    ``pyarmor_runtime_000000`` directory), attempts
    ``pyarmor gen --use-runtime runtime_dir ...`` to avoid regenerating
    the runtime on every invocation.  Falls back to a plain
    ``pyarmor gen`` if ``--use-runtime`` is not supported by the installed
    version.

    The obfuscation runs inside a private temp directory; only the
    resulting ``.py`` file is copied to *out_dir*.  Any per-run runtime
    folder generated by Pyarmor is discarded.

    Returns True if a non-trivially-small output file was written.
    """
    fname = os.path.basename(src_file)

    def _attempt(cmd: list[str]) -> Optional[str]:
        """
        Run *cmd* inside a temp directory.
        If obfuscation succeeds, copies the resulting .py to a new named
        temp file that outlives the temp directory, and returns that path.
        Returns None on failure.
        """
        with tempfile.TemporaryDirectory() as tmp:
            r = subprocess.run(cmd + ['-O', tmp, src_file],
                               capture_output=True, text=True)
            if r.returncode != 0:
                return None
            for root, _, files in os.walk(tmp):
                if 'pyarmor_runtime_000000' in root:
                    continue
                if fname in files:
                    candidate = os.path.join(root, fname)
                    if os.path.getsize(candidate) >= MIN_OBFUSCATED_SIZE:
                        # Copy out before the TemporaryDirectory is deleted
                        fd, keeper = tempfile.mkstemp(suffix='.py')
                        os.close(fd)
                        shutil.copy2(candidate, keeper)
                        return keeper
        return None

    found: Optional[str] = None

    # Try with shared runtime first (avoids redundant per-file runtime gen)
    if runtime_dir and os.path.isdir(runtime_dir):
        found = _attempt([pyarmor_bin, 'gen', '--use-runtime', runtime_dir])

    # Fall back to standard invocation
    if found is None:
        found = _attempt([pyarmor_bin, 'gen'])

    if found:
        try:
            os.makedirs(out_dir, exist_ok=True)
            shutil.copy2(found, os.path.join(out_dir, fname))
            return True
        finally:
            # Clean up the temporary keeper file
            try:
                os.unlink(found)
            except OSError:
                pass
    return False


# ======================================================================
#  Step 1 -- Python obfuscation
# ======================================================================

def step_python(base_dir: str, dist_dir: str, fernet_key: bytes) -> bool:
    """
    Stage and obfuscate small Python files with Pyarmor.
    For files that exceed SPLIT_THRESHOLD (cannot be encrypted by Pyarmor trial),
    compile them directly to .pyc files instead of splitting.

    Returns True only if every file was processed without error.
    """
    import py_compile
    _banner("STEP 1 - Python obfuscation (Pyarmor / .pyc compilation)")

    pyarmor_bin = _find_pyarmor(base_dir)
    _info(f"pyarmor : {pyarmor_bin}")

    # ── Generate the shared runtime once ─────────────────────────────────
    _info("Generating Pyarmor runtime ...")
    r = subprocess.run(
        [pyarmor_bin, 'gen', 'runtime', '-O', dist_dir],
        capture_output=True, text=True, cwd=base_dir,
    )
    if r.returncode != 0:
        _warn("Runtime generation failed:\n" + (r.stderr or r.stdout or '').strip()[:300])
        runtime_dir = None
    else:
        _ok("pyarmor_runtime_000000/ written to dist/")
        # Locate the generated runtime so _run_pyarmor_file can reuse it
        runtime_dir = os.path.join(dist_dir, 'pyarmor_runtime_000000')
        if not os.path.isdir(runtime_dir):
            runtime_dir = None

    # ── Build staging directory for Pyarmor ───────────────────────────────
    stage_dir = os.path.join(base_dir, STAGE_DIR_NAME)
    if os.path.exists(stage_dir):
        shutil.rmtree(stage_dir)
    os.makedirs(stage_dir)

    all_sources = _collect_py_files(base_dir)
    pyc_compiled_count = 0
    fail_count = 0

    for src in all_sources:
        rel         = os.path.relpath(src, base_dir)
        basename    = os.path.basename(src)
        rel_dir     = os.path.dirname(rel)
        out_sub     = os.path.join(dist_dir, rel_dir)
        os.makedirs(out_sub, exist_ok=True)

        # Replace core/loaders.py with the Fernet-key-embedded version
        if rel.replace('\\', '/') == 'core/loaders.py':
            dest_subdir = os.path.join(stage_dir, rel_dir)
            os.makedirs(dest_subdir, exist_ok=True)
            write_loader(os.path.join(dest_subdir, basename), fernet_key)
            _info("Injected Fernet key into staged core/loaders.py")
            continue

        size = os.path.getsize(src)
        if size > SPLIT_THRESHOLD:
            # Cannot be encrypted with Pyarmor. Compile to .pyc instead.
            stem = os.path.splitext(basename)[0]
            dest_pyc = os.path.join(out_sub, f"{stem}.pyc")
            try:
                py_compile.compile(src, cfile=dest_pyc, doraise=True)
                pyc_compiled_count += 1
                _info(f"[pyc]  {rel} ({size // 1024} KB) compiled directly to .pyc")
            except Exception as exc:
                fail_count += 1
                _fail(f"Compilation failed for {rel}: {exc}")
        else:
            # Copy to stage dir for Pyarmor obfuscation
            dest_subdir = os.path.join(stage_dir, rel_dir)
            os.makedirs(dest_subdir, exist_ok=True)
            shutil.copy2(src, os.path.join(dest_subdir, basename))

    # ── Obfuscate every staged .py with Pyarmor ───────────────────────────
    staged: list[str] = []
    for root, dirs, files in os.walk(stage_dir):
        dirs[:] = [d for d in dirs if d != '__pycache__']
        for f in sorted(files):
            if f.endswith('.py'):
                staged.append(os.path.join(root, f))

    pyarmor_ok_count = 0

    for src_file in staged:
        rel     = os.path.relpath(src_file, stage_dir)
        rel_dir = os.path.dirname(rel)
        out_sub = os.path.join(dist_dir, rel_dir)

        success = _run_pyarmor_file(pyarmor_bin, src_file, out_sub, runtime_dir)

        if success:
            pyarmor_ok_count += 1
            _info(f"[obf]  {rel}")
        else:
            fail_count += 1
            _fail(f"Pyarmor failed: {rel}")
            # Echo the actual Pyarmor error for diagnostics
            r2 = subprocess.run(
                [pyarmor_bin, 'gen', '-O', out_sub, src_file],
                capture_output=True, text=True,
            )
            print("    " + (r2.stderr or r2.stdout or "").strip()[:300],
                  file=sys.stderr)

    # ── Copy migrations as plain .py (Django requires them at runtime) ────
    for pkg in PYTHON_PACKAGES:
        mig_src = os.path.join(base_dir, pkg, 'migrations')
        if os.path.isdir(mig_src):
            mig_dst = os.path.join(dist_dir, pkg, 'migrations')
            shutil.copytree(mig_src, mig_dst, dirs_exist_ok=True,
                            ignore=shutil.ignore_patterns('__pycache__'))
            _ok(f"Copied plain migrations: {pkg}/migrations/")

    # ── Clean up stage ────────────────────────────────────────────────────
    shutil.rmtree(stage_dir, ignore_errors=True)

    print()
    _ok(f"Python: {pyarmor_ok_count} obfuscated, {pyc_compiled_count} compiled to .pyc, {fail_count} failed.")
    return fail_count == 0


# ======================================================================
#  Step 2 -- JavaScript obfuscation
# ======================================================================

def step_javascript(base_dir: str, dist_dir: str) -> None:
    _banner("STEP 2 - JavaScript obfuscation (server.js)")

    src      = os.path.join(base_dir, 'whatsapp_service', 'server.js')
    dest_sub = os.path.join(dist_dir, 'whatsapp_service')
    dest     = os.path.join(dest_sub, 'server.js')
    os.makedirs(dest_sub, exist_ok=True)

    if not os.path.isfile(src):
        _warn("whatsapp_service/server.js not found -- skipping.")
        return

    npx = shutil.which('npx') or 'npx'
    cmd = [
        npx, '--yes', 'javascript-obfuscator', src,
        '--output', dest,
        '--compact', 'true',
        '--identifier-names-generator', 'hexadecimal',
        '--string-array', 'true',
        '--string-array-encoding', 'rc4',
        '--self-defending', 'true',
        '--disable-console-output', 'false',
    ]

    _info("Running javascript-obfuscator via npx ...")
    result = subprocess.run(cmd, capture_output=True, text=True, cwd=base_dir)

    if result.returncode == 0 and os.path.isfile(dest):
        _ok(f"server.js  {os.path.getsize(src) // 1024} KB -> {os.path.getsize(dest) // 1024} KB")
        return

    # Retry with a minimal flag set for compatibility with older npx versions
    cmd_min = [npx, '--yes', 'javascript-obfuscator', src,
               '--output', dest, '--compact', 'true', '--self-defending', 'true']
    _info("Retrying with minimal flags ...")
    r2 = subprocess.run(cmd_min, capture_output=True, text=True, cwd=base_dir)
    if r2.returncode == 0 and os.path.isfile(dest):
        _ok("server.js obfuscated (minimal flags)")
    else:
        shutil.copy2(src, dest)
        _warn("javascript-obfuscator failed -- copied original server.js")
        err = (r2.stderr or r2.stdout or "").strip()
        if err:
            _info("Error: " + err[:400])


# ======================================================================
#  Step 3 -- Template Fernet encryption
# ======================================================================

def step_templates(base_dir: str, dist_dir: str, fernet_key: bytes) -> None:
    _banner("STEP 3 - Template encryption (Fernet / AES-128-CBC + HMAC)")

    src_root  = os.path.join(base_dir, 'templates')
    dest_root = os.path.join(dist_dir, 'templates')

    if not os.path.isdir(src_root):
        _warn("templates/ not found -- skipping.")
        return

    count = 0
    for root, _, files in os.walk(src_root):
        for filename in files:
            src_path  = os.path.join(root, filename)
            rel       = os.path.relpath(src_path, src_root)
            dest_path = os.path.join(dest_root, rel)
            os.makedirs(os.path.dirname(dest_path), exist_ok=True)

            with open(src_path, 'rb') as fh:
                data = fh.read()

            if filename.endswith(('.html', '.txt', '.xml')):
                if not data.startswith(ENC_MARKER):
                    data = fernet_encrypt(data, fernet_key)
                with open(dest_path, 'wb') as fh:
                    fh.write(data)
                count += 1
            else:
                shutil.copy2(src_path, dest_path)

    _ok(f"{count} template file(s) encrypted with Fernet.")


# ======================================================================
#  Step 4 -- Static files & configs
# ======================================================================

def step_assets(base_dir: str, dist_dir: str) -> None:
    _banner("STEP 4 - Copying static files & configs")

    for folder in ('static', 'staticfiles'):
        src = os.path.join(base_dir, folder)
        if os.path.isdir(src):
            shutil.copytree(src, os.path.join(dist_dir, folder), dirs_exist_ok=True)
            _ok(f"Copied {folder}/")

    for fname in ('db.sqlite3', 'license.enc', 'requirements.txt', '.gitignore'):
        src = os.path.join(base_dir, fname)
        if os.path.isfile(src):
            shutil.copy2(src, os.path.join(dist_dir, fname))
            _ok(f"Copied {fname}")

    ws_src  = os.path.join(base_dir, 'whatsapp_service')
    ws_dest = os.path.join(dist_dir, 'whatsapp_service')
    os.makedirs(ws_dest, exist_ok=True)
    for fname in ('package.json', 'package-lock.json'):
        src = os.path.join(ws_src, fname)
        if os.path.isfile(src):
            shutil.copy2(src, os.path.join(ws_dest, fname))
            _ok(f"Copied whatsapp_service/{fname}")


# ======================================================================
#  Step 5 -- npm install
# ======================================================================

def step_npm(dist_dir: str) -> None:
    _banner("STEP 5 - npm install (dist/whatsapp_service)")
    ws_dir = os.path.join(dist_dir, 'whatsapp_service')
    npm    = shutil.which('npm.cmd') or shutil.which('npm') or 'npm'

    if not os.path.isfile(os.path.join(ws_dir, 'package.json')):
        _warn("No package.json found -- skipping npm install.")
        return

    r = subprocess.run(
        [npm, 'install', '--omit=dev'],
        cwd=ws_dir, capture_output=True, text=True,
    )
    if r.returncode == 0:
        _ok("npm dependencies installed.")
    else:
        _warn("npm install failed (run manually inside dist/whatsapp_service/).")
        print((r.stderr or r.stdout or '').strip()[:300], file=sys.stderr)


# ======================================================================
#  Entry point
# ======================================================================

def main() -> int:
    parser = argparse.ArgumentParser(
        description="Build a Pyarmor-obfuscated dist/ copy of the project.",
    )
    parser.add_argument(
        '--clean',
        action='store_true',
        help="Delete existing dist/ (and dist_tmp/ if present) and exit.",
    )
    args = parser.parse_args()

    base_dir = os.path.dirname(os.path.abspath(__file__))
    dist_dir = os.path.join(base_dir, DIST_DIR_NAME)
    tmp_dir  = os.path.join(base_dir, DIST_TMP_NAME)

    if args.clean:
        for d in (dist_dir, tmp_dir):
            if os.path.exists(d):
                shutil.rmtree(d)
                print(f"Removed {d}")
        return 0

    print("=" * 66)
    print("  PROJECT OBFUSCATION BUILD TOOL")
    print(f"  Source  : {base_dir}")
    print(f"  Output  : {dist_dir}")
    print("=" * 66)

    # ── Atomic build: work inside tmp_dir; commit only on success ─────────
    if os.path.exists(tmp_dir):
        shutil.rmtree(tmp_dir)
    os.makedirs(tmp_dir)

    fernet_key = generate_fernet_key()
    _info("Generated fresh Fernet key for this build.")

    success = True
    try:
        py_ok = step_python(base_dir, tmp_dir, fernet_key)
        if not py_ok:
            _warn("Some Python files failed to obfuscate (see above).")
            success = False

        step_javascript(base_dir, tmp_dir)
        step_templates(base_dir, tmp_dir, fernet_key)
        step_assets(base_dir, tmp_dir)
        step_npm(tmp_dir)

    except Exception as exc:
        _fail(f"Unexpected build error: {exc}")
        shutil.rmtree(tmp_dir, ignore_errors=True)
        return 1

    # ── Commit: atomically replace dist/ ─────────────────────────────────
    # shutil.move handles cross-filesystem renames gracefully (falls back to
    # copytree + rmtree when os.rename would raise OSError).
    if not os.path.exists(tmp_dir):
        _fail("Build directory vanished before commit step -- aborting.")
        return 1
    if os.path.exists(dist_dir):
        shutil.rmtree(dist_dir)
    # Prefer os.rename (atomic on same filesystem); fall back to shutil.move
    # (copies across filesystems / drives when needed).
    try:
        os.rename(tmp_dir, dist_dir)
    except OSError:
        shutil.move(tmp_dir, dist_dir)

    print(f"\n{'=' * 66}")
    if success:
        print("  [DONE] Build complete!")
    else:
        print("  [DONE] Build complete (with warnings -- review output above).")
    print(f"  Output : {dist_dir}")
    print()
    print("  Deployment:")
    print("    1. Copy dist/ to the target machine.")
    print("    2. Create a venv and run:  pip install -r requirements.txt")
    print("    3. cd dist && python run_server.py")
    print('=' * 66)
    return 0 if success else 2


if __name__ == '__main__':
    sys.exit(main())
