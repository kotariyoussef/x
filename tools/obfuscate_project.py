def generate_fernet_key() -> bytes:
    """Return a new URL-safe base-64 encoded 32-byte Fernet key."""
    from cryptography.fernet import Fernet
    return Fernet.generate_key()


def fernet_encrypt(data: bytes, key: bytes) -> bytes:
    """Encrypt *data* with Fernet and prepend ENC_MARKER."""
    from cryptography.fernet import Fernet
    return ENC_MARKER + Fernet(key).encrypt(data)


# ======================================================================
#  loaders.py generator
#
#  core/loaders.py in the dist copy is regenerated to embed the Fernet
#  key before Pyarmor obfuscates it.  The key is NOT present in the
#  original source tree and never appears in plain text inside dist/.
# ======================================================================

LOADER_TEMPLATE = '''\
# Auto-generated by obfuscate_project.py.  Do NOT commit to VCS.
# The Fernet key below is embedded inside Pyarmor-obfuscated bytecode.
# Extracting it requires significant reverse-engineering effort, but it
# is not equivalent to hardware-backed secret storage.
from django.template.loaders.filesystem import Loader as FilesystemLoader
from django.template import TemplateDoesNotExist
from cryptography.fernet import Fernet as _Fernet

_FERNET_KEY = {key_repr}
_ENC_MARKER = {marker_repr}
_fernet     = _Fernet(_FERNET_KEY)


class DecryptedFilesystemLoader(FilesystemLoader):
    """Template loader that transparently decrypts Fernet-encrypted templates."""

    def get_contents(self, origin):
        try:
            with open(origin.name, 'rb') as fp:
                data = fp.read()
            if data.startswith(_ENC_MARKER):
                decrypted = _fernet.decrypt(data[len(_ENC_MARKER):])
                return decrypted.decode(self.engine.file_charset)
            return data.decode(self.engine.file_charset)
        except FileNotFoundError:
            raise TemplateDoesNotExist(origin)
'''


def write_loader(dest_path: str, key: bytes) -> None:
    """Write a Fernet-key-embedded loaders.py to *dest_path*."""
    os.makedirs(os.path.dirname(dest_path), exist_ok=True)
    code = LOADER_TEMPLATE.format(
        key_repr    = repr(key),
        marker_repr = repr(ENC_MARKER),
    )
    with open(dest_path, 'w', encoding='utf-8') as fh:
        fh.write(code)


