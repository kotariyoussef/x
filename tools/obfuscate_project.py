def _read_source(path: str) -> str:
    """Read a Python source file, trying the most common encodings."""
    for enc in ('utf-8-sig', 'utf-8', 'cp1252', 'latin-1'):
        try:
            with open(path, encoding=enc) as fh:
                return fh.read()
        except (UnicodeDecodeError, LookupError):
            continue
    raise ValueError(f"Cannot decode {path!r} with any known encoding.")


def _node_text(node: ast.stmt, lines: list[str]) -> str:
    """Return the exact source text of *node* (including trailing newline)."""
    return ''.join(lines[node.lineno - 1 : node.end_lineno])


# ======================================================================
#  AST module analysis helpers
# ======================================================================

def _collect_defined_names(nodes: list[ast.stmt]) -> set[str]:
    """
    Return the set of names *assigned* or *imported* by *nodes*.

    Handles:
    - ``import x``, ``import x as y``            -> {x} / {y}
    - ``from m import x``, ``from m import x as y`` -> {x} / {y}
    - ``x = ...``, ``x: T = ...``, ``x += ...``  -> {x}
    - ``x, y = ...`` and nested tuples/lists     -> {x, y}
    - ``def foo``, ``class Foo``                 -> {foo} / {Foo}
    """
    names: set[str] = set()

    def _extract_targets(node: ast.expr) -> None:
        if isinstance(node, ast.Name):
            names.add(node.id)
        elif isinstance(node, (ast.Tuple, ast.List)):
            for elt in node.elts:
                _extract_targets(elt)
        # starred, subscript, attribute targets are not plain names

    for nd in nodes:
        if isinstance(nd, ast.Import):
            for alias in nd.names:
                names.add(alias.asname if alias.asname else alias.name.split('.')[0])
        elif isinstance(nd, ast.ImportFrom):
            for alias in nd.names:
                if alias.name == '*':
                    continue
                names.add(alias.asname if alias.asname else alias.name)
        elif isinstance(nd, ast.Assign):
            for target in nd.targets:
                _extract_targets(target)
        elif isinstance(nd, (ast.AnnAssign, ast.AugAssign)):
            _extract_targets(nd.target)
        elif isinstance(nd, (ast.FunctionDef, ast.AsyncFunctionDef, ast.ClassDef)):
            names.add(nd.name)

    return names


def _has_unsafe_preamble(preamble_nodes: list[ast.stmt]) -> Optional[str]:
    """
    Return a human-readable reason if *preamble_nodes* contains constructs
    that cannot be safely referenced from another module; None if safe.

    The checks are conservative: a file is only flagged as unsafe if it
    clearly mutates global module state in ways that cannot be safely
    shared across a module boundary.
    """
    for nd in preamble_nodes:
        for child in ast.walk(nd):
            if isinstance(child, ast.Call):
                # exec(...) or eval(...)
                if isinstance(child.func, ast.Name) and child.func.id in ('exec', 'eval'):
                    return f"preamble calls {child.func.id}() at line {child.lineno}"
                # globals()[...] = ...  -- detected heuristically
                if (isinstance(child.func, ast.Attribute)
                        and isinstance(child.func.value, ast.Call)
                        and isinstance(child.func.value.func, ast.Name)
                        and child.func.value.func.id in ('globals', 'locals')
                        and child.func.attr in ('__setitem__', 'update')):
                    return f"preamble mutates globals()/locals() at line {child.lineno}"
    return None


def _names_referenced_in(node: ast.stmt) -> set[str]:
    """Return all Name ids that appear in the *load* context inside *node*."""
    return {
        n.id for n in ast.walk(node)
        if isinstance(n, ast.Name) and isinstance(n.ctx, ast.Load)
    }


def _analyze_module(
    tree: ast.Module,
    lines: list[str],
) -> tuple[list[ast.stmt], list[ast.stmt], list[ast.stmt], bool, str]:
    """
    Classify every top-level node in *tree* into one of three buckets.

    Returns
    -------
    import_nodes  : Import / ImportFrom statements  (safe to duplicate)
    preamble_nodes: all other non-def/class nodes that are safe to place
                    in part 1 before any definition (executed once only)
    def_nodes     : FunctionDef / AsyncFunctionDef / ClassDef nodes
                    (distributed across parts by size)
    is_safe       : False if the file cannot be split without changing semantics
    unsafe_reason : human-readable explanation when is_safe is False

    Splitting is unsafe when a non-import node that appears after a def/class
    **references a name defined by an earlier def/class**, because moving it to
    the preamble block (which precedes all definitions in part 1) would mean
    the reference resolves before the definition is executed.

    Pure-constant interleaved assignments (ones whose RHS only references
    builtins and import names, never a locally-defined function or class) are
    treated as late preamble and moved safely to part 1.

    Example of an *unsafe* interleaving::

        class Foo: ...
        REGISTRY = {'Foo': Foo}   # references local class Foo -> unsafe
        class Bar: ...

    Example of a *safe* interleaving::

        def helper(): ...
        MONTHS = {'jan': 1, 'feb': 2}   # no reference to helper -> safe
        def other(): ...
    """
    import_nodes:   list[ast.stmt] = []
    preamble_nodes: list[ast.stmt] = []
    def_nodes:      list[ast.stmt] = []

    # Accumulate locally-defined names as we walk in order
    locally_defined: set[str] = set()
    unsafe_at: Optional[int] = None
    unsafe_reason_str = ''

    for nd in tree.body:
        if isinstance(nd, (ast.Import, ast.ImportFrom)):
            import_nodes.append(nd)
            locally_defined |= _collect_defined_names([nd])
        elif isinstance(nd, (ast.FunctionDef, ast.AsyncFunctionDef, ast.ClassDef)):
            locally_defined.add(nd.name)
            def_nodes.append(nd)
        else:
            # Non-import, non-def node.  Check whether it references any
            # locally-defined function/class name.
            referenced = _names_referenced_in(nd)
            local_refs = referenced & locally_defined
            # Filter out names that came purely from imports (safe to duplicate)
            import_defined = _collect_defined_names(import_nodes)
            dangerous_refs = local_refs - import_defined

            if dangerous_refs and def_nodes:  # after first def AND uses local name
                if unsafe_at is None:
                    unsafe_at = nd.lineno
                    unsafe_reason_str = (
                        f"module has a statement at line {nd.lineno} that "
                        f"references locally-defined name(s) "
                        f"{sorted(dangerous_refs)!r} after a function/class "
                        f"definition.  Moving it to the preamble would change "
                        f"execution order."
                    )
            preamble_nodes.append(nd)
            locally_defined |= _collect_defined_names([nd])

    if unsafe_at is not None:
        return import_nodes, preamble_nodes, def_nodes, False, unsafe_reason_str

    unsafe_preamble = _has_unsafe_preamble(preamble_nodes)
    if unsafe_preamble:
        return import_nodes, preamble_nodes, def_nodes, False, unsafe_preamble

    return import_nodes, preamble_nodes, def_nodes, True, ''


# ======================================================================
#  AST-based file splitter
# ======================================================================

def split_file(
    src_path: str,
    dest_dir: str,
    threshold: int = SPLIT_THRESHOLD,
) -> list[str]:
    """
    Split a large Python source file into several *_partN.py* files and
    write a thin re-export shim under the original filename.

    Algorithm
    ---------
    1.  Parse the module and classify nodes with ``_analyze_module``.
    2.  If the module is **not safe to split**, copy it as-is, emit a
        warning, and return ``[dest]`` so the caller can still attempt
        Pyarmor obfuscation on the single file.
    3.  Build **part 1**: import text + preamble text + first batch of
        definitions that fit within *threshold*.
    4.  Build **parts 2 +**: import text + a single cross-import line that
        pulls every preamble-defined name from part 1 + their definitions.
        The cross-import references already-evaluated objects; it does NOT
        re-execute the preamble.
    5.  Validate every generated part with ``ast.parse()``.  Abort the
        build (raise ``RuntimeError``) if any part has invalid syntax.
    6.  Propagate ``__all__`` to the shim so star-import filtering works.
    7.  Write the shim as the original filename in *dest_dir*.

    Returns
    -------
    List of written part-file paths (shim NOT included).
    If the file was not split (too small, or unsafe), returns a list
    containing only the single destination path.
    """
    source    = _read_source(src_path)
    raw_bytes = source.encode('utf-8')
    basename  = os.path.basename(src_path)
    stem      = os.path.splitext(basename)[0]

    # ── Trivially small: just copy ───────────────────────────────────────
    if len(raw_bytes) <= threshold:
        dest = os.path.join(dest_dir, basename)
        with open(dest, 'w', encoding='utf-8') as fh:
            fh.write(source)
        return [dest]

    # ── Parse ────────────────────────────────────────────────────────────
    lines: list[str] = source.splitlines(keepends=True)
    try:
        tree = ast.parse(source, filename=src_path)
    except SyntaxError as exc:
        _warn(f"{basename}: AST parse failed ({exc}) -- kept as single file.")
        dest = os.path.join(dest_dir, basename)
        with open(dest, 'w', encoding='utf-8') as fh:
            fh.write(source)
        return [dest]

    # ── Analyse ──────────────────────────────────────────────────────────
    import_nodes, preamble_nodes, def_nodes, is_safe, unsafe_reason = (
        _analyze_module(tree, lines)
    )

    if not is_safe:
        _warn(
            f"{basename}: cannot split safely -- {unsafe_reason}  "
            f"Keeping as single file ({len(raw_bytes)//1024} KB); "
            f"Pyarmor will attempt to obfuscate it directly."
        )
        dest = os.path.join(dest_dir, basename)
        with open(dest, 'w', encoding='utf-8') as fh:
            fh.write(source)
        return [dest]

    if not def_nodes:
        # Nothing to split (only imports / preamble)
        dest = os.path.join(dest_dir, basename)
        with open(dest, 'w', encoding='utf-8') as fh:
            fh.write(source)
        return [dest]

    # ── Build text segments ───────────────────────────────────────────────
    imports_text  = ''.join(_node_text(n, lines) for n in import_nodes)
    preamble_text = ''.join(_node_text(n, lines) for n in preamble_nodes)
    imports_bytes = len(imports_text.encode('utf-8'))
    preamble_bytes= len(preamble_text.encode('utf-8'))

    # Names that parts 2+ need to reference from part 1
    preamble_names = _collect_defined_names(preamble_nodes)
    # Also collect names from imports that appear in the preamble block
    # (they're already duplicated via imports_text, so don't cross-import)
    import_names   = _collect_defined_names(import_nodes)
    cross_names    = preamble_names - import_names  # only non-import preamble

    cross_import_text = ''
    if cross_names:
        names_str = ', '.join(sorted(cross_names))
        cross_import_text = f"from .{stem}_part1 import {names_str}\n"

    part1_prefix_bytes = imports_bytes + preamble_bytes
    partN_prefix_bytes = imports_bytes + len(cross_import_text.encode('utf-8'))

    # ── Greedy size-based grouping of def_nodes ───────────────────────────
    # Part 1 budget accounts for preamble; parts 2+ account for cross-import.
    chunks: list[list[ast.stmt]] = []
    current: list[ast.stmt] = []
    is_first_chunk = True
    current_prefix = part1_prefix_bytes
    current_size   = current_prefix

    for nd in def_nodes:
        ns = _node_text(nd, lines)
        nb = len(ns.encode('utf-8'))

        if nb + current_prefix > threshold:
            # This single definition is already larger than the budget.
            # Give it its own chunk and emit a warning — Pyarmor's actual
            # limit is higher than our conservative threshold.
            if current:
                chunks.append(current)
                current = []
            _warn(
                f"{basename}: definition at line {nd.lineno} "
                f"({nb // 1024} KB) exceeds threshold; "
                f"placed in its own chunk — Pyarmor may still accept it."
            )
            chunks.append([nd])
            is_first_chunk = False
            current_prefix = partN_prefix_bytes
            current_size   = current_prefix
            continue

        if current_size + nb > threshold:
            chunks.append(current)
            current        = [nd]
            is_first_chunk = False
            current_prefix = partN_prefix_bytes
            current_size   = current_prefix + nb
        else:
            current.append(nd)
            current_size += nb

    if current:
        chunks.append(current)

    if len(chunks) <= 1:
        # Splitting produced only one chunk — no point in splitting
        dest = os.path.join(dest_dir, basename)
        with open(dest, 'w', encoding='utf-8') as fh:
            fh.write(source)
        return [dest]

    # ── Generate and validate part files ─────────────────────────────────
    part_paths: list[str] = []

    for idx, chunk in enumerate(chunks, start=1):
        defs_text = ''.join(_node_text(n, lines) for n in chunk)

        if idx == 1:
            part_text = imports_text + '\n' + preamble_text + '\n' + defs_text
        else:
            part_text = imports_text + '\n' + cross_import_text + '\n' + defs_text

        # Validate before writing
        try:
            ast.parse(part_text, filename=f"<{stem}_part{idx}>")
        except SyntaxError as exc:
            raise RuntimeError(
                f"BUG: generated {stem}_part{idx} has invalid syntax: {exc}\n"
                f"Please report this with the original file."
            ) from exc

        part_name = f"{stem}_part{idx}.py"
        part_path = os.path.join(dest_dir, part_name)
        with open(part_path, 'w', encoding='utf-8') as fh:
            fh.write(part_text)
        part_paths.append(part_path)

    # ── Build the re-export shim ──────────────────────────────────────────
    shim_lines: list[str] = [
        "# Auto-generated re-export shim.  Do not edit.\n",
        "# Imports from each part in definition order.\n",
    ]

    # Propagate __all__ so star-import filtering is preserved
    for nd in preamble_nodes:
        if (isinstance(nd, ast.Assign)
                and any(
                    isinstance(t, ast.Name) and t.id == '__all__'
                    for t in nd.targets
                )):
            shim_lines.append(_node_text(nd, lines))
            break

    for idx in range(1, len(part_paths) + 1):
        shim_lines.append(f"from .{stem}_part{idx} import *\n")

    # Validate the shim syntax (it's simple but worth checking)
    shim_text = ''.join(shim_lines)
    try:
        ast.parse(shim_text, filename=f"<{stem} shim>")
    except SyntaxError as exc:
        raise RuntimeError(f"BUG: generated shim for {basename} has invalid syntax: {exc}") from exc

    shim_path = os.path.join(dest_dir, basename)
    with open(shim_path, 'w', encoding='utf-8') as fh:
        fh.writelines(shim_lines)

    return part_paths


