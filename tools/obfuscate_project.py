# -*- coding: utf-8 -*-
"""
obfuscate_project.py
====================
Builds a fully-protected copy of this Django project into the ``dist/`` folder.

Protection layers
-----------------
  Python   -> Pyarmor 9 obfuscation for every .py file.
               Files that exceed SPLIT_THRESHOLD are split at top-level
               class/function boundaries into _part1.py, _part2.py, ...;
               the original filename becomes a thin re-export shim.
               Module-level preamble (loggers, constants, decorators, etc.)
               is placed in part 1 and referenced via an explicit import in
               all subsequent parts, so Python/Django semantics are
               preserved exactly.

  JS       -> javascript-obfuscator via npx (whatsapp_service/server.js)

  Templates-> Fernet symmetric encryption (AES-128-CBC + HMAC-SHA256).
               A fresh key is generated on every build and written into
               the dist copy of core/loaders.py before Pyarmor obfuscates
               it.  The key is embedded inside obfuscated bytecode, which
               significantly increases the reverse-engineering effort needed
               to extract it, but this is NOT equivalent to hardware-backed
               key storage (e.g. an HSM or TPM).  A sufficiently determined
               attacker with process-memory access or Pyarmor deobfuscation
               capability may still recover the key.

  Static   -> copied as-is (intentionally unprotected)

Usage
-----
  cd <project-root>
  python obfuscate_project.py           # normal build
  python obfuscate_project.py --clean   # delete dist/ only

Re-running is always safe; a new dist/ is built from scratch each time.
The build is atomic: work happens inside ``dist_tmp/`` which is renamed to
``dist/`` only on success.  A failed build leaves ``dist/`` untouched.
"""

